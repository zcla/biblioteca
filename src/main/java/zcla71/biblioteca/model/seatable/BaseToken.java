package zcla71.biblioteca.model.seatable;

public class BaseToken {
    private String app_name;
    private String access_token;
    private String dtable_uuid;
    private String dtable_server;
    private String dtable_socket;
    private String dtable_db;
    private String workspace_id;
    private String dtable_name;

    public String getApp_name() {
        return app_name;
    }

    public void setApp_name(String app_name) {
        this.app_name = app_name;
    }

    public String getAccess_token() {
        return access_token;
    }

    public void setAccess_token(String access_token) {
        this.access_token = access_token;
    }

    public String getDtable_uuid() {
        return dtable_uuid;
    }

    public void setDtable_uuid(String dtable_uuid) {
        this.dtable_uuid = dtable_uuid;
    }

    public String getDtable_server() {
        return dtable_server;
    }

    public void setDtable_server(String dtable_server) {
        this.dtable_server = dtable_server;
    }

    public String getDtable_socket() {
        return dtable_socket;
    }

    public void setDtable_socket(String dtable_socket) {
        this.dtable_socket = dtable_socket;
    }

    public String getDtable_db() {
        return dtable_db;
    }

    public void setDtable_db(String dtable_db) {
        this.dtable_db = dtable_db;
    }

    public String getWorkspace_id() {
        return workspace_id;
    }

    public void setWorkspace_id(String workspace_id) {
        this.workspace_id = workspace_id;
    }

    public String getDtable_name() {
        return dtable_name;
    }

    public void setDtable_name(String dtable_name) {
        this.dtable_name = dtable_name;
    }

}
